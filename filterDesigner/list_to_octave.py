def python_list_to_octave(lst):
    octave_str = "[ "
    for num in lst:
        # Para asegurarnos de que los números en notación científica estén en un formato que Octave entienda:
        formatted_num = str(num)
        octave_str += formatted_num + "; "
    # Remover el último punto y coma y espacio, y cerrar el bracket
    octave_str = octave_str[:-2] + " ]"
    return octave_str


def print_string_in_lines(s, max_length=80):
    start = 0
    end = max_length

    while start < len(s):
        if end < len(s) and s[end] != ";":
            while end > start and s[end] != ";":
                end -= 1
            end += 1  # Para incluir el ';'

        print(s[start:end].strip())  # strip() elimina espacios al principio y al final
        start = end
        end = start + max_length


